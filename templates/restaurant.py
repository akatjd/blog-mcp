from tools.style_analyzer import load_style_profile, build_style_instruction


def build_restaurant_prompt(info: dict) -> str:
    name = info.get("name", "")
    location = info.get("location", "")
    menu = info.get("menu", "")
    price_range = info.get("price_range", "")
    rating = info.get("rating", "")
    extra = info.get("extra", "")

    profile = load_style_profile()
    style_section = f"\n{build_style_instruction(profile)}\n" if profile else ""

    return f"""[카테고리: 맛집 리뷰]

기본 정보:
- 식당명: {name}
- 위치: {location}
- 주요 메뉴: {menu}
- 가격대: {price_range}
- 별점: {rating}/5
{f"- 추가 메모: {extra}" if extra else ""}
{style_section}
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
블로그 작성 규칙:
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

1. 출력 형식 (절대 원칙):
   - 마크다운 문법 절대 사용 금지 (네이버 블로그는 마크다운 미지원)
   - ## 헤더, **굵은글씨**, --- 구분선, 표(|), 불릿(-/*), 인용구(>) 모두 금지
   - 일반 텍스트와 줄바꿈만 사용

2. 분량: 1500자 이상 (공백 포함) — 체류시간 확보를 위해 충분한 분량 필수

2. 톤/문체:
   - 30대 중반 남성, 솔직하고 담백한 후기 스타일
   - 과장 없이 친구한테 말하듯 자연스럽게
   - "~했다", "~였다" 체 사용 (블로그 일반체)
   - 맛 표현은 구체적으로 (예: "달달하면서 짭조름한", "불향이 은근히 배어있는")

3. 이미지 캡션:
   - 각 사진 분석 후 [📷 {name} - 설명] 형식으로 본문에 삽입
   - 캡션은 사진 내용을 구체적으로 설명 (단순 "음식 사진" X)
   - 사진은 최소 5장 이상 활용 권장

4. 제목 형식 (반드시 아래 형식 준수):
   - 형식: [{location}] {name} - 대표메뉴명
   - 예시: [왕십리/상왕십리] 아라참치 - 스페셜
   - 예시: [을지로3가역] 달맞이광장바베큐 - 소금바베큐
   - 예시: [일본/나고야] 야바톤 센트라이즈 점(미소카츠)
   - 국내: [역이름] 또는 [동네/인근동네] 형식
   - 해외: [나라/도시] 형식
   - 부제목, 추가 문구, 느낌표 등 넣지 말 것

5. 키워드 전략 (DIA 점수 향상):
   - 핵심 키워드("{location} 맛집", "{name}")를 제목·도입부·중반·마무리에 자연스럽게 2~3회 반복
   - 동의어·연관어도 활용 (예: 맛집 → 맛있는 곳, 식당, 가게)
   - 키워드 억지 반복 금지 — 자연스럽게 녹여야 함

6. 네이버 지도 장소 태그 안내:
   - 본문 마지막에 아래 문구 삽입:
     "[{name} 위치 정보는 네이버 지도에서 확인하세요]"
   - 실제 발행 시 네이버 지도 장소 태그 필수 추가 (검색 노출에 직결)

7. 해시태그 (5~7개, 짧은 키워드형):
   - 필수: {location}맛집, {name}, {menu} 관련 태그
   - 예: #{location}맛집 #{name} #혼밥맛집 #가성비맛집

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
블로그 구조 (이 순서로 작성):
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

제목: (SEO 최적화된 제목)

도입부 (약 200자)
  └ 방문 계기, 첫인상 또는 기대감
  └ 핵심 키워드 자연스럽게 포함

분위기·인테리어 (약 250자)
  └ 매장 외관, 좌석, 인테리어, 혼잡도
  └ 📷 캡션 삽입

메뉴 소개 및 맛 평가 (약 600자)
  └ 주문한 메뉴별 맛, 양, 플레이팅 평가
  └ 가격 구체적으로 언급
  └ 📷 캡션 삽입 (2~3장)

가격 & 접근성 (약 200자)
  └ 가격 대비 만족도, 주차/대중교통 정보
  └ 핵심 키워드 재등장

총평 (약 150자)
  └ 한 줄 결론 + 추천 대상
  └ 재방문 의향 언급 (체류시간·재방문율 지표에 영향)

네이버 지도 장소 태그 안내 문구

해시태그
"""
