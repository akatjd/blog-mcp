def build_restaurant_prompt(info: dict) -> str:
    name = info.get("name", "")
    location = info.get("location", "")
    menu = info.get("menu", "")
    price_range = info.get("price_range", "")
    rating = info.get("rating", "")
    extra = info.get("extra", "")

    return f"""[카테고리: 맛집 리뷰]

기본 정보:
- 식당명: {name}
- 위치: {location}
- 주요 메뉴: {menu}
- 가격대: {price_range}
- 별점: {rating}/5
{f"- 추가 메모: {extra}" if extra else ""}

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
블로그 작성 규칙:
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

1. 분량: 1000자 이상 (공백 포함)

2. 톤/문체:
   - 30대 중반 남성, 솔직하고 담백한 후기 스타일
   - 과장 없이 친구한테 말하듯 자연스럽게
   - "~했다", "~였다" 체 사용 (블로그 일반체)
   - 맛 표현은 구체적으로 (예: "달달하면서 짭조름한", "불향이 은근히 배어있는")

3. 이미지 캡션:
   - 각 사진 분석 후 [📷 {name} - 설명] 형식으로 본문에 삽입
   - 캡션은 사진 내용을 구체적으로 설명 (단순 "음식 사진" X)

4. SEO 최적화 제목:
   - 식당명 + 지역 + 핵심 키워드 포함
   - 예: "{location} [{name}] 솔직 후기 - 혼밥하기 좋은 로컬 맛집"

5. 해시태그: 7~10개 (지역명맛집, 식당명, 메뉴명, 분위기 키워드)

━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
블로그 구조 (이 순서로 작성):
━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━

제목: (SEO 최적화된 제목)

도입부 (약 150자)
  └ 방문 계기, 첫인상 또는 기대감

분위기·인테리어 (약 200자)
  └ 매장 외관, 좌석, 인테리어, 혼잡도 + 📷 캡션

메뉴 소개 및 맛 평가 (약 450자)
  └ 주문한 메뉴별 맛, 양, 플레이팅 평가 + 📷 캡션

가격 & 접근성 (약 150자)
  └ 가격 대비 만족도, 주차/대중교통

총평 (약 100자)
  └ 한 줄 결론, 추천 대상 (예: "가성비 점심 찾는 직장인에게 추천")

해시태그
"""
